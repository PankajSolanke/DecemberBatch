public class SwitchCaseExample {
    public static void main(String[] args) {
        int day = 3 ;
        switch (day) {
            case 1 :
                System.out.println("Its Monday");
            break;
            case 2 :
                System.out.println("Its Tuesday");
            break;
            case 3 :
                System.out.println("Its Wednesday");
            break;
            default:
                System.out.println("Invalid Input");
        }
    }
}
